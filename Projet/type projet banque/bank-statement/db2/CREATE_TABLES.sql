-- TABLE CLIENTS
CREATE TABLE BANKDB.CLIENTS (
    CLIENT_ID       CHAR(10)        NOT NULL,
    NOM             VARCHAR(40)     NOT NULL,
    PRENOM          VARCHAR(40)     NOT NULL,
    ADRESSE         VARCHAR(80)     NOT NULL,
    VILLE           VARCHAR(30)     NOT NULL,
    CODE_POSTAL     CHAR(5)         NOT NULL,
    NUM_COMPTE      CHAR(16)        NOT NULL,
    DATE_OUVERTURE  DATE            NOT NULL,
    STATUT          CHAR(1)         NOT NULL DEFAULT 'A',
    CONSTRAINT PK_CLIENTS PRIMARY KEY (CLIENT_ID),
    CONSTRAINT UQ_COMPTE  UNIQUE      (NUM_COMPTE),
    CONSTRAINT CK_STATUT  CHECK       (STATUT IN ('A','I','S'))
);

-- TABLE TRANSACTIONS
CREATE TABLE BANKDB.TRANSACTIONS (
    TRANS_ID        CHAR(12)        NOT NULL,
    NUM_COMPTE      CHAR(16)        NOT NULL,
    DATE_TRANS      DATE            NOT NULL,
    HEURE_TRANS     TIME            NOT NULL,
    TYPE_TRANS      CHAR(3)         NOT NULL,
    MONTANT         DECIMAL(15,2)   NOT NULL,
    SOLDE_APRES     DECIMAL(15,2)   NOT NULL,
    LIBELLE         VARCHAR(50)     NOT NULL,
    STATUT_TRANS    CHAR(1)         NOT NULL DEFAULT 'V',
    CONSTRAINT PK_TRANS   PRIMARY KEY (TRANS_ID),
    CONSTRAINT FK_COMPTE  FOREIGN KEY (NUM_COMPTE)
                          REFERENCES BANKDB.CLIENTS(NUM_COMPTE),
    CONSTRAINT CK_TYPE    CHECK (TYPE_TRANS IN
                          ('CRE','DEB','VIR','RET','DEP','FRA')),
    CONSTRAINT CK_STATUT  CHECK (STATUT_TRANS IN ('V','A','R'))
);

CREATE INDEX BANKDB.IX_TRANS_CPT ON BANKDB.TRANSACTIONS
       (NUM_COMPTE, DATE_TRANS DESC);

