-- Suppression des tables existantes
DROP TABLE IF EXISTS EMPRUNT;
DROP TABLE IF EXISTS LIVRE;
DROP TABLE IF EXISTS ADHERENT;

-- Table ADHERENT
CREATE TABLE ADHERENT (
    ID           CHAR(6) PRIMARY KEY,
    NOM          CHAR(30),
    PRENOM       CHAR(20),
    ADRESSE      CHAR(50),
    DATE_ADHESION DATE,
    PRIME        DECIMAL(7,2)
);

-- Table LIVRE
CREATE TABLE LIVRE (
    ISBN         CHAR(17) PRIMARY KEY,
    TITRE        CHAR(50),
    AUTEUR       CHAR(30),
    CATEGORIE    CHAR(20),
    QUANTITE     INTEGER
);

-- Table EMPRUNT
CREATE TABLE EMPRUNT (
    ID           CHAR(6) PRIMARY KEY,
    ID_ADHERENT  CHAR(6),
    ISBN         CHAR(17),
    DATE_EMPRUNT DATE,
    DATE_RETOUR  DATE,
    AMENDE       DECIMAL(5,2)
);

