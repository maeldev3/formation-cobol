-- Suppression des tables existantes
DROP TABLE IF EXISTS INDEMNITE;
DROP TABLE IF EXISTS SINISTRE;
DROP TABLE IF EXISTS CONTRAT;
DROP TABLE IF EXISTS CLIENT;

-- Table CLIENT
CREATE TABLE CLIENT (
    ID         CHAR(8) PRIMARY KEY,
    NOM        CHAR(30),
    ADRESSE    CHAR(50),
    TEL        CHAR(15)
);

-- Table CONTRAT
CREATE TABLE CONTRAT (
    NUMERO      CHAR(10) PRIMARY KEY,
    ID_CLIENT   CHAR(8),
    TYPE        CHAR(3),
    PRIME       DECIMAL(9,2),
    DATE_DEBUT  DATE
);

-- Table SINISTRE
CREATE TABLE SINISTRE (
    ID           CHAR(10) PRIMARY KEY,
    NUM_CONTRAT  CHAR(10),
    DATE_SIN     DATE,
    MONTANT_DEM  DECIMAL(9,2),
    STATUS       CHAR(1)
);

-- Table INDEMNITE
CREATE TABLE INDEMNITE (
    ID_SINISTRE  CHAR(10),
    MONTANT      DECIMAL(9,2),
    DATE_PAIE    DATE
);

